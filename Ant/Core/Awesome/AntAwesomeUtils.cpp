QVariantMap AntAwesomeUtils::getFAObject(FontAwesomeType id)
{
    QVariantMap obj;
    const QString DefaultFontFamily = "Font Awesome 6 Free";
    const QString BrandsFontFamily = "Font Awesome 6 Brands";
    obj["weight"] = QFont::Normal;
    obj["text"] = getFAUnicode(id);
    if (BrandsS.contains(id)) {
        obj["fontFamily"] = BrandsFontFamily;
        return obj;
    }else {
        obj["fontFamily"] = DefaultFontFamily;
    }

    if (RegularS.contains(id)) {
        obj["weight"] = obj["weight"] = QFont::Normal;
    }else {
        obj["weight"] = QFont::Black;
    }
    return obj;
}

QChar AntAwesomeUtils::getFAUnicode(FontAwesomeType id)
{
    return QChar(id);
}
